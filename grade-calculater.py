score = int(input("Enter your marks: "))

if score >= 90:
    print("Congrats! Your grade is 'A' ")
elif score >= 80:
    print("Congrats! Your grade is 'B' ")
elif score >= 70:
    print("Congrats! Your grade is 'C' ")
elif score >= 60:
    print("Congrats! Your grade is 'D' ")
else:
    print("Your grade is 'F' ")

