def add_numbers(num1,num2):
    sum1 = num1 + num2
    return sum1

def product_numbers(num1, num2):
    product = num1 * num2
    return product

num1 = 12
num2 = 12

sum_of_numbers = add_numbers(num1, num2)
product_of_numbers = product_numbers(num1, num2)

print('Sum of numbers:', sum_of_numbers)
print('Product of numbers:', product_of_numbers)